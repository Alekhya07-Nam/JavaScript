var equaldiv=document.getElementsByClassName("num15")[3];
function equal(){
    // equaldiv.style.boxShadow="0px 0px 2px 2px grey";
    var maindivsEqulas=document.getElementsByClassName("innerFirstDiv")[0];
    let maindivsEqulasContent=maindivsEqulas.innerHTML;
    let finalString=maindivsEqulasContent.split('');
    console.log(finalString);
    let stack=[];
    let operations=[];
    for(var i=0;i<finalString.length;i++){
        if(finalString[i]>='0' && finalString[i]<='9'){
            let temp="";
            while(i<finalString.length && finalString[i]>='0' && finalString[i]<='9'){
                temp+=finalString[i++];
            }
            let number=parseInt(temp,10);
            console.log(number);
            stack.push(number);
            i--;
        }
        else if(finalString[i]==')'){
            operations.push(finalString[i]);
        }
        else if(finalString[i]=='('){
            while(operations[operations.length-1]!='('){
                stack.push(performOPeration(operations.pop(),stack.pop(),stack.pop()));
            }
            operations.pop();
        }
        else if(finalString[i]=='+' || finalString[i]=='-' || finalString[i]=='*' || finalString[i]=='/'){
            while(operations.length>0 && precedence(finalString[i],operations[operations.length-1])){
                stack.push(performOPeration(operations.pop(),stack.pop(),stack.pop()));
            }
            operations.push(finalString[i]);
        }
    }
    while(operations.length>0){
        stack.push(performOPeration(operations.pop(),stack.pop(),stack.pop()));
    }
    console.log(stack[0]);
    maindivsEqulas.innerHTML=stack.pop();
    var bubble1=document.getElementsByClassName("num15")[3];
    bubble1.style.animation="bubble 0.1s ease-in";
}

function precedence(operator1,operator2){
    if(operator1=="(" || operator2==")"){
        return false;
    }
    if((operator1 == '*' || operator1 == '/') &&
    (operator2 == '+' || operator2 == '-')){
        return false;
    }
    else{
        return true;
    }
}

function performOPeration(op, b, a)
    {
        switch (op)
        {
        case '+':
            return a + b;
        case '-':
            return a - b;
        case '*':
            return a * b;
        case '/':
            if (b == 0)
            {
                document.write("Cannot divide by zero");
            }
            return parseInt(a / b, 10);
        }
        return 0;
    }

